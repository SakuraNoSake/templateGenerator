    // Валидация ИНН
    function validateINN(inn) {
        if (!inn) return false;
        const cleanedInn = inn.replace(/\D/g, '');
        if (cleanedInn.length !== 10 && cleanedInn.length !== 12) {
            return false;
        }
        return /^\d+$/.test(cleanedInn);
    }
    
