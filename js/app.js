    function getCurrentTimestamp() {
        const now = new Date();
        const dateStr = now.toISOString().slice(0, 10).replace(/-/g, ''); 
        const timeStr = now.toTimeString().slice(0, 8).replace(/:/g, ''); 
        return `${dateStr}_${timeStr}`;
    }

    //генератор СНИЛС (Проверка последнего числа)
    function calculateSNILSCheckSum(numbers) {
        let sum = 0;
        for (let i = 0; i < 9; i++) {
            sum += parseInt(numbers[i]) * (9 - i);
        }
        let checkSum = sum % 101;
        if (checkSum === 100) {
            checkSum = 0;
        }
        return checkSum;
    }
    
    // Генератор СНИЛС
    function generateSNILS() {
        let numbers = '';
        for (let i = 0; i < 9; i++) {
            numbers += Math.floor(Math.random() * 10);
        }
        let checkSum = calculateSNILSCheckSum(numbers);
        let formattedCheckSum = checkSum.toString().padStart(2, '0');
        let formattedSNILS = 
            numbers.substring(0, 3) + '-' + 
            numbers.substring(3, 6) + '-' + 
            numbers.substring(6, 9) + ' ' + 
            formattedCheckSum;
        return formattedSNILS;
    }

