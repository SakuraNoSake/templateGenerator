    // Генерация случайной даты рождения (от 0 до 7 лет)
    function getChildBirthDate() {
        const today = new Date();
        // Максимальная дата рождения: сегодня (0 лет)
        const maxDate = today;
        // Минимальная дата рождения: 7 лет назад
        const minDate = new Date();
        minDate.setFullYear(today.getFullYear() - 7);
        // Генерируем случайную дату между minDate и maxDate
        const randomTimestamp = minDate.getTime() + Math.random() * (maxDate.getTime() - minDate.getTime());
        const randomDate = new Date(randomTimestamp);
        // Форматируем дату в DD.MM.YYYY
        const day = String(randomDate.getDate()).padStart(2, '0');
        const month = String(randomDate.getMonth() + 1).padStart(2, '0');
        const year = randomDate.getFullYear();
        return `${day}.${month}.${year}`;
    }

